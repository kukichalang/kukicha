package main

import (
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"strings"
	"testing"
)

func TestStripGeneratedHeader(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  string
	}{
		{
			name:  "standard header",
			input: "// Generated by Kukicha (requires Go 1.26+)\n\npackage main\n",
			want:  "package main\n",
		},
		{
			name:  "no header",
			input: "package main\n",
			want:  "package main\n",
		},
		{
			name:  "header without blank line",
			input: "// Generated by Kukicha (requires Go 1.26+)\npackage main\n",
			want:  "package main\n",
		},
		{
			name:  "empty input",
			input: "",
			want:  "",
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := string(stripGeneratedHeader([]byte(tt.input)))
			if got != tt.want {
				t.Errorf("stripGeneratedHeader() = %q, want %q", got, tt.want)
			}
		})
	}
}

func TestBrewProducesValidGo(t *testing.T) {
	// Find example .kuki files
	examples, err := filepath.Glob(filepath.Join("..", "..", "examples", "*.kuki"))
	if err != nil {
		t.Fatal(err)
	}
	if len(examples) == 0 {
		t.Skip("no example files found")
	}

	for _, example := range examples {
		name := filepath.Base(example)
		t.Run(name, func(t *testing.T) {
			absPath, err := filepath.Abs(example)
			if err != nil {
				t.Fatal(err)
			}

			// Compile with line directives stripped (same as brew)
			cr, err := compile(absPath, "", "", true)
			if err != nil {
				t.Fatalf("compile failed: %v", err)
			}
			clean := stripGeneratedHeader(cr.formatted)

			// Verify it parses as valid Go
			fset := token.NewFileSet()
			_, parseErr := parser.ParseFile(fset, name+".go", clean, parser.AllErrors)
			if parseErr != nil {
				t.Errorf("brewed output is not valid Go: %v\n\nOutput:\n%s", parseErr, clean)
			}

			// Verify no Kukicha artifacts remain
			output := string(clean)
			if strings.Contains(output, "Generated by Kukicha") {
				t.Error("brewed output still contains 'Generated by Kukicha' header")
			}
			if strings.Contains(output, "//line ") {
				t.Error("brewed output still contains //line directives")
			}
		})
	}
}

func TestBrewWritesFile(t *testing.T) {
	// Create a minimal .kuki file in a temp dir
	tmpDir := t.TempDir()
	kukiFile := filepath.Join(tmpDir, "hello.kuki")
	if err := os.WriteFile(kukiFile, []byte("func main()\n    print(\"hello\")\n"), 0644); err != nil {
		t.Fatal(err)
	}

	// Brew it
	brewCommand(kukiFile, false, false)

	// Check .go file was created
	goFile := filepath.Join(tmpDir, "hello.go")
	data, err := os.ReadFile(goFile)
	if err != nil {
		t.Fatalf("expected %s to exist: %v", goFile, err)
	}

	output := string(data)
	if strings.Contains(output, "Generated by Kukicha") {
		t.Error("brewed file contains generated header")
	}
	if !strings.Contains(output, "package main") {
		t.Error("brewed file missing package declaration")
	}
}
